declare(strict_types=1);

namespace Flavioski\Module\SalusPerAquam\WebService;

use SoapClient;

class SoapClientNG extends SoapClient
{
    public function __doRequest($request, $location, $action, $version = SOAP_1_1, $oneWay = false)
    {
        $xml = explode("\r\n", parent::__doRequest($request, $location, $action, $version, $oneWay));

        $response = preg_replace('/^(\x00\x00\xFE\xFF|\xFF\xFE\x00\x00|\xFE\xFF|\xFF\xFE|\xEF\xBB\xBF)/', '', $xml[0]);

        return $response;
    }
}
